--    - 低库存预警: product_id=2,4 (可用库存 < 预警阈值)
--    - 缺货状态: product_id=5
--    - 下架状态: product_id=6
--
-- 2. 库存日志操作类型:
--    - IN: 入库操作 (初始入库、补货入库)
--    - OUT: 出库操作 (订单出库)
--    - RESERVE: 预留操作 (订单预留、秒杀预留)
--    - RELEASE: 释放操作 (订单取消)
--    - ADJUST: 调整操作 (损坏、过期等)
--    - COUNT: 盘点操作 (盘点差异调整)
--
-- 3. 库存盘点状态:
--    - PENDING: 待确认 (3条记录,包含盘盈、盘亏场景)
--    - CONFIRMED: 已确认 (3条记录,已自动调整库存)
--    - CANCELLED: 已取消 (2条记录,盘点作废)
